#Q.1) WAP to find greatest among two numbers

#Take input two numbers from user
num1=int(input("Enter First Number:"))
num2=int(input("Enter Second Number:"))

# Comparison of two numbers for finding the greatest number
if num1 > num2 :
    print(f"The number {num1} is Greatest")
else:
    print(f"The number {num2} is Greatest")
